package et.ts.bean;

public class MusicBean {
    private String songname;
    private String singername;
    private String songnd;
    private String m4a;
    private String alb_big;

    public String getSongname() {
        return songname;
    }

    public void setSongname(String songname) {
        this.songname = songname;
    }

    public String getSingername() {
        return singername;
    }

    public void setSingername(String singername) {
        this.singername = singername;
    }

    public String getSongnd() {
        return songnd;
    }

    public void setSongnd(String songnd) {
        this.songnd = songnd;
    }

    public String getM4a() {
        return m4a;
    }

    public void setM4a(String m4a) {
        this.m4a = m4a;
    }

    public String getAlb_big() {
        return alb_big;
    }

    public void setAlb_big(String alb_big) {
        this.alb_big = alb_big;
    }
}
